// Nested loop
#include<stdio.h>

int main()
{
    for(int i=1; i==1; i++)
    {
        for(int j=1; j==1; j++)
        {
            printf("*");
        }
        printf("\n");
        
        for(int k=1; k<=3; k++)
        {
            printf("*");
        }
        printf("\n");
        
        for(int k=1; k<=5; k++)
        {
            printf("*");
        }
        printf("\n");
        
        for(int k=1; k<=7; k++)
        {
            printf("*");
        }
        printf("\n");
        
        for(int k=1; k<=9; k++)
        {
            printf("*");
        }
        printf("\n");
        
        for(int k=1; k<=11; k++)
        {
            printf("*");
        }
        printf("\n");
    }

    return 0;
}